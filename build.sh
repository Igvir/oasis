#!bin/bash

echo "==============================================================================="
echo "Building Oasis..."
echo "==============================================================================="
mvn install -DskipTests

echo "==============================================================================="
echo "Building Events API Docker Image..."
echo "==============================================================================="
cd services/events-api
docker build -t oasis/events-api .

cd ../..

echo "==============================================================================="
echo "Starting Oasis..."
echo "==============================================================================="
docker-compose up



